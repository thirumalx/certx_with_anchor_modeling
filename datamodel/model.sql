-- Difference perspective ---------------------------------------------------------------------------------------------
-- dCE_Certificate showing all differences between the given timepoints and optionally for a subset of attributes
-----------------------------------------------------------------------------------------------------------------------
 CREATE OR REPLACE FUNCTION certx.dCE_Certificate 
      ( intervalStart timestamp,
        intervalEnd timestamp,
        selection text = null
      )
RETURNS TABLE 
      ( inspectedTimepoint timestamp
      , mnemonic text
      , CE_ID bigint
      , Metadata_CE bigint
      , CE_ION_CE_ID bigint
      , Metadata_CE_ION bigint
      , CE_ION_Certificate_IssuedOn timestamp with time zone
      , CE_RON_CE_ID bigint
      , Metadata_CE_RON bigint
      , CE_RON_Certificate_RevokedOn timestamp with time zone
      , CE_SNO_CE_ID bigint
      , Metadata_CE_SNO bigint
      , CE_SNO_Certificate_SerialNumber varchar(25)
      , CE_LTV_CE_ID bigint
      , Metadata_CE_LTV bigint
      , CE_LTV_Certificate_LastTimeVerifiedOn timestamp with time zone
      , CE_CEP_CE_ID bigint
      , Metadata_CE_CEP bigint
      , CE_CEP_Certificate_CertificatePath text
      , CE_NAF_CE_ID bigint
      , Metadata_CE_NAF bigint
      , CE_NAF_Certificate_NotAfter timestamp with time zone
      , CE_STA_CE_ID bigint
      , Metadata_CE_STA bigint
      , CE_STA_ChangedAt timestamp
      , CE_STA_STA_Status varchar(45)
      , CE_STA_Metadata_STA bigint 
      , CE_STA_STA_ID smallint 
      ) 
AS 
'
 SELECT timepoints.inspectedTimepoint
      , timepoints.mnemonic
      , pCE.*
   FROM (
          SELECT DISTINCT CE_STA_CE_ID AS CE_ID
               , CE_STA_ChangedAt::timestamp inspectedTimepoint
               , ''STA'' AS mnemonic
            FROM certx.CE_STA_Certificate_Status
           WHERE (selection is null OR selection like ''%STA%'')
             AND CE_STA_ChangedAt BETWEEN intervalStart AND intervalEnd
        ) timepoints
  CROSS 
   JOIN LATERAL certx.pCE_Certificate(timepoints.inspectedTimepoint) pCE
  WHERE pCE.CE_ID = timepoints.CE_ID;
' 
LANGUAGE SQL STABLE
;
